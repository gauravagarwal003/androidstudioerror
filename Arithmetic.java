package examples.pltw.org;

public class Arithmetic {
    private int mOperand1;
    private int mOperand2;

    public Arithmetic() {
        mOperand1 = 2;
        mOperand2 = 3;
    }

    public String toString() {
        return "Arithmetic Instance: mOperand1 = " + mOperand1 + "; mOperand 2 = " + mOperand2;
    }
}
